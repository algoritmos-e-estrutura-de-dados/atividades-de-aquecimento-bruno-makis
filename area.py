A=float(input("Digite a o primeiro número flutuante: "))
B=float(input("Digite o segundo número flutuante: "))
C=float(input("Digite o terceiro, e último, número flutuante: "))

triangulo = ((A*C)/2)
print(f"TRIANGULO: {triangulo:.3F}")

circulo = ((3.14159)*(C**2))
print(f"CIRCULO: {circulo:.3F}")

trapezio = ((A+B)*(C)/2)
print(f"TRAPEZIO: {trapezio:.3F}")

quadrado = (B*B)
print(f"QUADRADO: {quadrado:.3F}")

retangulo = (A*B)
print(f"RETANGULO: {retangulo:.3F}")